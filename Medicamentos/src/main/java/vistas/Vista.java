    public void mostrarListaCompleta(List<Medicamentos> lista) {
        StringBuilder sb = new StringBuilder("Lista de Medicamentos:\n");
        for (int i = 0; i < lista.size(); i++) {
            Medicamentos med = lista.get(i);
            sb.append(i).append(": ").append(med.getNombre()).append(" - ").append(med.getMarca()).append(" - ").append(med.getPrecio()).append("\n");
        }
        JOptionPane.showMessageDialog(this, sb.toString());
    }
